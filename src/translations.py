def get_text(lang: str, key: str, **kwargs) -> str:
    """Get translated text for a given language and key, with optional format arguments."""
    text = UI_TEXT.get(lang, UI_TEXT["English"]).get(key, UI_TEXT["English"].get(key, key))
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, IndexError):
            return text
    return text


def get_language_options() -> list:
    """Return list of (display_label, language_key) tuples for the language selector."""
    options = []
    for lang_key, meta in LANGUAGES.items():
        label = f"{meta['flag']} {meta['native_name']} ({lang_key})"
        options.append((label, lang_key))
    return options
